# type casting
# its a method that changing from one data type to another data type

a = float (10)
print(a, type(a))

b = int(10.88)
print(b, type(b))

c = str(10)
print(c, type(c))

d = int("12345")
print(d, type(d))

e = bool("123")
print(e, type(e))
